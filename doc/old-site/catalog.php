  <p> 44 page, 7''x7'' booklet featuring illustrated lyrics and supplemental writing by The President. Comes with a CD that pairs EXPERIENCE SOBRIETY alongside The President's out of print first album, LEBRON, and additional bonus tracks including covers, alternate takes and forbidden B-sides. 33 SONGS. The studio: a digital 8-track in the center of The President's private(ly squatted) estate in Cutler Bay, The White House. The genre: a unique, highly personalized bastardization of 60 years worth of rock music, paired with a linear, associative/freestyle lyrical cadence and subject matter. The President is sober. The President is a 3rd Degree Master Mason. The President is a criminal, on probation, spreading an ideology rooted in positive thinking and positive feeling, as to ultimately manifest positive living. The President invokes the immersion-based street smarts of Burroughs and Bukowski, the life-affirming gusto of 80s Youth Crew hardcore, the psychic empowerment of self-help/get rich literature and the frenetic, referential nature of contemporary digital discourse. </p>
