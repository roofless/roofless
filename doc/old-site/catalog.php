<p><strong>RR01<br />
  The Fas’ners / The Würst <br />
  Live @ Sarasota Su</strong><strong>cculent Society CS <br />
    OUT OF PRINT </strong></p>
<p>Live recordings from a house party on March 9, 2007. Tyger Beat, Space Manatee, Pharaoh Faucett and The Sharks also played. Attendees did everything they could to lose their minds (footprints on the living room ceiling). The whole night was recorded on to a tape recorder left on the kitchen table. The only band that didn’t make the cut was Pharaoh Faucett and there was a reason. That same reason led to the tape being &quot;lost&quot; only to be found again because it wasn't lost in the first place. </p>
<p>Culled from these sessions is the last set from Tampa's THE FAS'NERS: pleasure in the future, ups and downs, swelling grand piano synthesizer music. 13 minutes 40 seconds. On the B-side is a 4 minute 53 second dirty dirge (and first set) from Sarasota's THE WÜRST: like being in the middle of a volcano in the middle of Jupiter in the middle of da Universe (wild, whipping synth + hheeaavvyy guitar) cover art by Allie Weiss. 99 of these tapes exist.</p>
