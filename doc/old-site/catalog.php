  <p><strong>RR08 <br />
The President - LeBron Deluxe CD-R<br />
$5 PPD </strong></p>
  <p>8 releases deep and this is our  first rock record. Drawing from power pop, metal, psychedelia and post-genre babble, <em>LeBron</em> was recorded on to a digital 8-track in a non-stop 4 day fury, culminating in The President's own ecstatic delirium upon learning of the Miami Heat's acquisition of LeBron James. This is an album about the   war between self-conception and self-actualization. Other themes   include: an obsessive lust for power and money, grandiose regionalism,   and  dark pleasure. <em>LeBron</em> is the product of it's surroundings: Planet White House, The President's   rambling compound in Cutler Bay; a residence with no furniture (ample   space for skateboarding), hundreds of alien drawings (some of which   include extraterrestrial villains ordering the artist to &quot;stop drawing us.&quot;), a vaporizer and portraits of George Washington alongside The President himself. 15 tracks of bizarre, infinitely clever/ridiculous   rock and roll that draws influence from Cheap Trick right on down to The   Melvins. Has been described as a &quot;one man Ween&quot; and &quot;a meglomaniac.&quot; The package: a CD-R inside a 7''   slip with an original drawing (one of a kind for each copy) by the man   himself, a one-sheet/factsheet/trackl ist, and a supplementary booklet filled with additional notes on the project.</p>
